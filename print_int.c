#include "ft_printf.h"

int	print_int(int nbr)
{
	int	i;

	ft_putnbr_fd(nbr, 1);
	i = 0;
	if (nbr < 0)
		i++;
	while (nbr / 10)
	{
		i++;
		nbr /= 10;
	}
	i++;
	return (i);
}
